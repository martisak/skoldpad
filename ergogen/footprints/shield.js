function mirror(body) {
    return body
        .replace(/\(layer "F\.(Cu|Mask|SilkS)"\)/g, '(layer "B.$1")')
        .replace(/\((start|end|xy) (-?[0-9.]+) (-?[0-9.]+)\)/g,
            (m, tag, x, y) => `(${tag} ${x} ${negate(y)})`);
}

function negate(n) {
    return n[0] === '-' ? n.slice(1) : (parseFloat(n) === 0 ? n : `-${n}`);
}

